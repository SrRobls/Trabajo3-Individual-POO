    private void btnHallarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHallarActionPerformed
        Double A,B,C;
        String resultado;
        try{
            A = Double.parseDouble(txtA.getText());
            B = Double.parseDouble(txtB.getText());
            C = Double.parseDouble(txtC.getText());
            
            resultado = Logica.hallarEcuacion(A, B, C);
            if (A == 0){
                resultado = "Recuerda que A no puede ser 0";
            }
            
            txtEcuacion.setText(resultado);
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Los valores ingresado esta en formato Incorrecto.");
            txtA.setText("");
            txtB.setText("");
            txtC.setText("");
        }
    }//GEN-LAST:event_btnHallarActionPerformed

