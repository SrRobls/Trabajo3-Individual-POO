    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Double patrimonio, pago;
        Integer estrato;
        
        try{
            patrimonio = Double.parseDouble(txtPatrimonio.getText());
            estrato = Integer.parseInt(txtEstrato.getText());
            
            pago = Logica.PagoMatricula(patrimonio, estrato);
            
            JOptionPane.showMessageDialog(null,"El estudiante con numero de inscripci√≥n " + txtNumero.getText() + " y nombre " + txtNombre.getText()
            + " Tiene que hacer un pago de matricula de " + pago);
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Los valores ingresados esta en formato Incorrecto.");
            txtPatrimonio.setText("");
            txtEstrato.setText("");
        }
            
    }//GEN-LAST:event_btnCalcularActionPerformed

