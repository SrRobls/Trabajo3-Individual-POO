    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Double base, altura;
        try{
            base = Double.parseDouble(txtBase.getText());
            altura = Double.parseDouble(txtAltura.getText());

            txtArea.setText(String.valueOf(main.metodosTrianguloRectangulo.calcularAreas(base, altura)));
            txtPerimetro.setText(String.valueOf(main.metodosTrianguloRectangulo.calcularPerimetro(base, altura)));
            txtHipotenusa.setText(String.valueOf(main.metodosTrianguloRectangulo.calcularHipotenusa(base, altura)));
            txtTipo.setText(main.metodosTrianguloRectangulo.determinarTipoTriangulo(base, altura));

        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"El valor ingresado esta en formato Incorrecto.");
            txtBase.setText("");
            txtAltura.setText("");
            txtHipotenusa.setText("");
            txtTipo.setText("");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

